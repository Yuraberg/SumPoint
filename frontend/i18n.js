let lang = localStorage.getItem("sp_lang") || "en";

function t(key, ...args) {
  let s = (I18N[lang] && I18N[lang][key]) || I18N.en[key] || key;
  args.forEach(a => { s = s.replace("%s", a); });
  return s;
}

function categoryLabel(v) {
  return (CATEGORY_LABELS[lang] && CATEGORY_LABELS[lang][v]) || v;
}

function eventTypeLabel(v) {
  return (EVENT_TYPE_LABELS[lang] && EVENT_TYPE_LABELS[lang][v]) || v;
}

function localeDate() {
  return lang === "en" ? "en-US" : "ru-RU";
}

function setLang(next) {
  localStorage.setItem("sp_lang", next);
  location.reload();
}

function applyI18n() {
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll("[data-i18n-title]").forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });

  // Category <option>s / checkboxes keep their Russian `value`; only the
  // visible label is translated.
  document.querySelectorAll("#filter-category option[value], #m-categories input[type=checkbox]").forEach(el => {
    if (!el.value) return;
    if (el.tagName === "OPTION") el.textContent = categoryLabel(el.value);
    else {
      const label = el.closest("label.cat-check");
      if (label) label.lastChild.textContent = " " + categoryLabel(el.value);
    }
  });
  document.querySelectorAll("#ev-filter-type option[value]").forEach(el => {
    if (el.value) el.textContent = eventTypeLabel(el.value);
  });

  const langBtn = document.getElementById("lang-btn");
  if (langBtn) langBtn.textContent = t("sidebar.lang");
}
